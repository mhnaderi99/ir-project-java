public class DocDouble {

    private Document doc;
    private Double doub;

    public DocDouble(Document doc, Double doub) {
        this.doc = doc;
        this.doub = doub;
    }

    public void setDoc(Document doc) {
        this.doc = doc;
    }

    public void setDoub(Double doub) {
        this.doub = doub;
    }

    public Document getDoc() {
        return doc;
    }

    public Double getDoub() {
        return doub;
    }
}
